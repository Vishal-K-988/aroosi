// Default country code (UK)
export const defaultCountryCode =
  countryCodes.find((c) => c.code === "+44") || countryCodes[0];

// Helper function to parse phone number with country code
export function parsePhoneNumber(phoneNumber: string): {
  countryCode: string;
  number: string;
} {
  // Remove all non-digit characters except +
  const cleaned = phoneNumber.replace(/[^\d+]/g, "");

  // Find matching country code
  const matchedCountry = countryCodes.find((country) =>
    cleaned.startsWith(country.code),
  );

  if (matchedCountry) {
    return {
      countryCode: matchedCountry.code,
      number: cleaned.substring(matchedCountry.code.length),
    };
  }

  // If no country code found, assume it's just the number
  return {
    countryCode: defaultCountryCode.code,
    number: cleaned.replace(/^\+/, ""),
  };
}

// Helper function to format phone number
export function formatPhoneNumber(countryCode: string, number: string): string {
  // Remove all non-digit characters from the local part
  const digitsOnly = number.replace(/\D/g, "");

  return `${countryCode} ${digitsOnly}`;
}
